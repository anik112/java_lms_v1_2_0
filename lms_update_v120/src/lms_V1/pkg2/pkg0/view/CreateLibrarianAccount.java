    private void btnDeleteLibrarianAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteLibrarianAccountActionPerformed
        // TODO add your handling code here:
        librarianService = new LibrarianService();
        if (librarianID > 0) {
            librarianService.deleteLibrarianInformation(librarianID);
            clearLibrarianSearchOption();
            clearTableForLibrarianInformation();
            lblShowDelLibrarianAccountPan.setText("Delete This Librarian Information successful");
        }
        librarianID = 0;
    }//GEN-LAST:event_btnDeleteLibrarianAccountActionPerformed

    private void btnSearchLibrarianAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchLibrarianAccountActionPerformed
        // TODO add your handling code here:

        librarianService = new LibrarianService();

        librarianAccount = librarianService.searchLibrarianAccountByCodeNo(Integer.parseInt(txtLibrarianAccountSearchBar.getText()));
        if (librarianAccount.getName() != null) {
            librarianID = librarianAccount.getId();
            lblShowLibrarianName.setText(librarianAccount.getName());
            lblShowLibrarianCodeNo.setText(String.valueOf(librarianAccount.getCodeNumber()));
            lblShowLibrarianMobileNumber.setText(librarianAccount.getContactInfo().getMobileNumber());
            lblShowLibrarianEmail.setText(librarianAccount.getContactInfo().getEmail());
            lblShowLibrarianAddress.setText(
                librarianAccount.getAddressInfo().getHouseNo()
                + ", " + librarianAccount.getAddressInfo().getRoadNo()
                + ", " + librarianAccount.getAddressInfo().getBlockOrVillage()
                + ", " + librarianAccount.getAddressInfo().getThana()
                + ", " + librarianAccount.getAddressInfo().getDistrict()
                + ", " + librarianAccount.getAddressInfo().getDivision()
            );
            lblShowDelLibrarianAccountPan.setText(null);
        } else {
            lblShowDelLibrarianAccountPan.setText("This code number not available in databese");
        }
    }//GEN-LAST:event_btnSearchLibrarianAccountActionPerformed

