    private void btnAddLibrarianRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLibrarianRecordActionPerformed
        // TODO add your handling code here:

        librarianAccount = new LibrarianAccount();
        librarianService = new LibrarianService();

        librarianAccount.setName(txtLibrarianName.getText());
        librarianAccount.setCodeNumber(Integer.parseInt(txtLibrarianCardNo.getText()));
        librarianAccount.setContactInfo(txtLibrarianMobileNo.getText(), txtLibrarianEmail.getText());
        librarianAccount.setAddressInfo(
            txtLibrarianHouseNo.getText(),
            txtLibrarianRoadNo.getText(),
            txtLibrarianBlockOrvVillage.getText(),
            txtLibrarianThana.getText(),
            comboLibrarianDistrict.getSelectedItem().toString(),
            comboLibrarianDivision.getSelectedItem().toString()
        );
        if (radioBtnShiftMorning.isSelected()) {
            librarianAccount.setShift("Morning");
        } else if (radioBtnShiftDay.isSelected()) {
            librarianAccount.setShift("Day");
        }
        librarianAccount.setUserName(txtLibrarianUserName.getText());

        if ((txtLibrarianPassword.getText()).equals(txtLibrarianConfirmPassword.getText())) {
            librarianAccount.setPassword(txtLibrarianPassword.getText());

            librarianService.saveLibrarianInformation(librarianAccount);
            lblShowMsgInLibrarianAccountPan.setText("Record add successful");

            resetLibrarianInformation();
        } else {
            txtLibrarianConfirmPassword.setForeground(Color.red);
            lblShowMsgInLibrarianAccountPan.setText("*/ Please CONFIRM your password /*");
        }

        clearTableForLibrarianInformation();
    }//GEN-LAST:event_btnAddLibrarianRecordActionPerformed

